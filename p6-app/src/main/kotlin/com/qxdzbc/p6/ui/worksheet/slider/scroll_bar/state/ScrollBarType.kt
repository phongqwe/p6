package com.qxdzbc.p6.ui.worksheet.slider.scroll_bar.state


/**
 * For differentiating between vertical (for row) and horizontal (for cols) scroll bars
 */
enum class ScrollBarType{
    /**
     * Vertical scroll bar type, for scrolling rows
     */
    Vertical,

    /**
     * Horizontal scroll bar type, for scrolling columns
     */
    Horizontal
}